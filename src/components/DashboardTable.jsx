export default function DashboardTable({ shopId }) {
  const [productsList, setProductsList] = useState(null);
  const [fetching, setFetching] = useState(false);
  const [page, setPage] = useState(1);
  const [totalRecords, setTotalRecords] = useState(null);

  const PAGE_SIZE = 10;

  useEffect(() => {
    getProductsList(page);
  }, []);

  const setNewPage = async (page) => {
    setPage(page);
    await getProductsList(page);
  };

  const getProductsList = async (page) => {
    try {
      setFetching(true);

      const from = (page - 1) * PAGE_SIZE;
      const to = from + PAGE_SIZE;

      const { data, count } = await supabase
        .from("products")
        .select("*", { count: "exact" })
        .eq("shop_id", shopId)
        .range(from, to);

      setProductsList(data);
      setTotalRecords(count);
    } catch (error) {
      console.log(error);
    } finally {
      setFetching(false);
    }
  };

  return (
    <DataTable
      height={450}
      withBorder
      borderRadius="md"
      shadow="sm"
      withColumnBorders
      highlightOnHover
      horizontalSpacing="sm"
      noRecordsText="No Products to show"
      records={productsList}
      fetching={fetching}
      totalRecords={totalRecords}
      recordsPerPage={PAGE_SIZE}
      page={page}
      onPageChange={(p) => setNewPage(p)}
      columns={[
        {
          accessor: "id",
          title: "#",
        },
        { accessor: "product_name", title: "Name" },
        { accessor: "product_brand", title: "Brand" },
        { accessor: "product_type", title: "Type" },
        { accessor: "product_mrp", title: "MRP" },
        { accessor: "product_price", title: "Price" },
      ]}
    />
  );
}
